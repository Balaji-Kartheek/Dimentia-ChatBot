def _bundle(lang: str) -> dict[str, str]:
    lang = (lang or DEFAULT_LANGUAGE).split("-")[0].lower()
    if lang == "hi":
        return {**_EN, **_HI_OVERRIDES}
    if lang == "ta":
        return {**_EN, **_TA_OVERRIDES}
    if lang in SUPPORTED_LANGUAGES:
        return dict(_EN)
    return dict(_EN)


def t(ui_lang: str, key: str, **kwargs) -> str:
    """Translate UI key; unknown keys fall back to English string or the key itself."""
    table = _bundle(ui_lang)
    text = table.get(key) or _EN.get(key) or key
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text


def welcome_body(user_role: str, lang: str) -> str:
    role = "caregiver" if user_role == "caregiver" else "user"
    return t(lang, f"welcome.{role}")
